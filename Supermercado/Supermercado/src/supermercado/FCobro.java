    public void calcular(){
        try {
            float suma=0;
            float iva=0;
            float sub = 0;
            for(int i=0; i < tableTicket.getRowCount(); i++)
            {
                double subTotal= Double.valueOf(tableTicket.getValueAt(i,2).toString());
                suma+=subTotal;
            } 
            iva+=suma*0.16;
            sub+=suma-iva;
            acumulado+= suma;
            lblsubtotal.setText("Subtotal: $"+String.valueOf(sub)+" MXN");
            lbliva.setText("IVA: $"+String.valueOf(iva)+" MXN");
            lbltotal.setText("Cobro Total: $"+String.valueOf(suma)+" MXN");
            lblAcumulado.setText("$"+acumulado);
        } catch (Exception e) {
            Logger.getLogger(FCobro.class.getName()).log(Level.SEVERE, null, e);
        }
        
    }

