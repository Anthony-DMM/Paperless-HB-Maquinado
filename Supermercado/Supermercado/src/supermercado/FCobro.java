    private void btnSigCobActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSigCobActionPerformed
        // TODO add your handling code here:
        try {
            cobro.setCarritos(cobro.pop(cobro.getCarritos()));
            if(cobro.getCarritos()!=null){
                numcarrito++;
                lblCarritoNum.setText("Número de carrito: "+numcarrito);
                Carrito datos = cobro.getCarritos().getDato();
                productos= new Vector<Producto>();
                productos= datos.toArray(datos.getProductos(), productos);
                modeloticket = new ModeloTicket(productos);
                tableTicket.setModel(modeloticket);
                lblAcumulado.setText("$"+acumulado);
                calcular();
            }else{
                JOptionPane.showMessageDialog(null, "Se han cobrado todos los carritos de esta caja");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Se han cobrado todos los carritos de esta caja");
        }
        
        
    }//GEN-LAST:event_btnSigCobActionPerformed

