    private void btnCaja1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCaja1ActionPerformed
        // TODO add your handling code here:
        if(carrito!=null){
            if(carrito.getProductos()!=null){
                caja1.setCarritos(caja1.push(caja1.getCarritos(), carrito));
                lblpersonasc1.setText("Carritos formados: "+caja1.count(caja1.getCarritos()));
                JOptionPane.showMessageDialog(null, "Carrito formado en Caja 1");
                carrito = null;
                vacio=true;
                btnRegresar.setEnabled(false);
                btnNuevoCarrito.setEnabled(true);
            }
        }else{
            JOptionPane.showMessageDialog(null, "No tienes un carrito");
        }
    }//GEN-LAST:event_btnCaja1ActionPerformed

